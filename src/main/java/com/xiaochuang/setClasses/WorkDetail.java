package com.xiaochuang.setClasses;

public class WorkDetail {
    private int workid;
    private String worktarget;
    private String targettext;
    private int emotionresulit;


    public int getWorkid() {
        return workid;
    }

    public void setWorkid(int workid) {
        this.workid = workid;
    }

    public String getWorktarget() {
        return worktarget;
    }

    public void setWorktarget(String worktarget) {
        this.worktarget = worktarget;
    }

    public String getTargettext() {
        return targettext;
    }

    public void setTargettext(String targettext) {
        this.targettext = targettext;
    }

    public int getEmotionresulit() {
        return emotionresulit;
    }

    public void setEmotionresulit(int emotionresulit) {
        this.emotionresulit = emotionresulit;
    }
}
